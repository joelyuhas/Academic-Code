/**********************************************************
 * computer_task(void* argument)
 **********************************************************
 * 
 * the computer task responsible for updating and controling
 * the computer serov as well as the point and output system
 *
 **********************************************************/
void computer_task(void* argument) {
	char buf[100];
	char buf2[100];
	char buf3[100];
	sprintf(buf2,"----------\n\r");
	while(1) {
		// still within 5 seconds
		if (total_counter < 10){
			if (timer_counter <= 50){
			 // checking if player is withing +/- 5% of the computer position
			 if (player_servo_position <= (computer_servo_position + (computer_servo_position*0.05)) && player_servo_position >= (computer_servo_position -(computer_servo_position*0.05))){
				 points = points + (10*combo);
				 combo = combo *2;
				 timer_counter = 0;
				 
				 buf2[total_counter] = '$';
				 
				 // set next position
				 computer_servo_position = (((HAL_RNG_GetRandomNumber(&Rng_Handle)) % 1501) + 500.0);
				 
				 set_pwm_pulse(&htim5, TIM_CHANNEL_1, computer_servo_position);
				 total_counter++;
			 } else {
				 buf2[total_counter] = '-';
			 }
			 timer_counter++;
			 
		 // timer outside 5 seconds
		 } else {
			 buf2[total_counter] = '!';
			 timer_counter = 0;
			 combo = 1;

			 computer_servo_position = (((HAL_RNG_GetRandomNumber(&Rng_Handle)) % 1501) + 500.0);
			 // set next position
			 set_pwm_pulse(&htim5, TIM_CHANNEL_1, computer_servo_position);
			 total_counter++;
			 
		 }

		 vPrintString(buf2);
		 
		 sprintf(buf3, "Turn: %d\t Points: %d\t Combo: %2.0d\t Time: %2.0d\t Position: %4.0d\t %4.0d\r\n", total_counter, points, combo, timer_counter, (int)player_servo_position, (int)computer_servo_position);
		 vPrintString(buf3);
 
		 sprintf(buf3, "\033[2A");
		 vPrintString(buf3);
		 
		 } else {
			 if(total_counter == 10){
			 sprintf(buf, "\n\n\n\rFinal Score: %d\n\r", points);
			 vPrintString(buf);
			 }
			 total_counter++;
		 }
	 osDelay(100);
	}
}
	

/**********************************************************
 * player_task(void* argument)
 **********************************************************
 * 
 * the player task responsible for handleing player 
 * operations
 *
 **********************************************************/
void player_task(void* argument) {
	
	while(1) {
		BSP_GYRO_GetXYZ(gyro_velocity_1);   // get raw values from gyro device
 
		// Getting gyro values
    for(int ii=0; ii<3; ii++) {
      gyro_angle_1[ii] = (int32_t)(gyro_velocity_1[ii] / GYRO_THRESHOLD_DETECTION);
    }
		
		// integrating gyro values into servo values	
		player_servo_position += gyro_angle_1[2];
		if (player_servo_position <= 500){
			player_servo_position = 500;
		} else if (player_servo_position >= 2000){
			player_servo_position = 2000;
		}

		set_pwm_pulse(&htim5, TIM_CHANNEL_2, (20000-player_servo_position));

		osDelay(10);
	}
}


